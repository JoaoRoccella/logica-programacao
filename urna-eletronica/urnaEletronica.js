const opcao = 3;

switch (opcao) {
    case 1:
        console.log('Opção é 1');
        break;
    case 2:
        console.log('Opção é 2');
        break;
    default:
        console.log('Nenhuma das opções');
        break;
}

if (opcao === 1) {
    console.log('Opção é 1');
} else if (opcao === 2) {
    console.log('Opção é 2');
} else {
    console.log('Nenhuma das opções');
}